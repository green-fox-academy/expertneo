public class Sweet extends CandyShop {
  double price;
  int sugarNeeded;

  public void setPrice(double price) {
    this.price = price;
  }

  public void setSugarNeeded(int sugarNeeded) {
    this.sugarNeeded = sugarNeeded;
  }

  public double getPrice() {
    return price;
  }

  public int getSugarNeeded() {
    return sugarNeeded;
  }
}
